async def process_user_query(user_query: str) -> str:
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∑–∞–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–µ—Ä–µ–∑ –º–æ–¥—É–ª—å query_llm.py
    
    Args:
        user_query (str): –ó–∞–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        
    Returns:
        str: –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∞–Ω–∞–ª–∏–∑–∞
    """
    try:
        logger.info(f"–û–±—Ä–∞–±–æ—Ç–∫–∞ –∑–∞–ø—Ä–æ—Å–∞: {user_query}")
        
        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –¥–ª—è –ø–µ—Ä–∏–æ–¥–∞ 2021-2024
        result_2124 = process_query(
            tables_csv_path=TABLES_CSV_PATH_2124,
            start_year=PERIOD_2124['start_year'],
            end_year=PERIOD_2124['end_year'],
            user_query=user_query
        )
        
        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –∑–∞–ø—Ä–æ—Å–∞ –¥–ª—è –ø–µ—Ä–∏–æ–¥–∞ 2016-2020
        result_1620 = process_query(
            tables_csv_path=TABLES_CSV_PATH_1620,
            start_year=PERIOD_1620['start_year'],
            end_year=PERIOD_1620['end_year'],
            user_query=user_query
        )
        
        # –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –∏ –∞–Ω–∞–ª–∏–∑ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
        combined_analysis = analyze_combined_results(user_query, result_2124, result_1620)
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω—É–∂–Ω–æ –ª–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å Tavily –ø–æ–∏—Å–∫
        if combined_analysis.get("result_type") == "not_found":
            logger.info("–î–∞–Ω–Ω—ã–µ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ CSV, –∑–∞–ø—É—Å–∫–∞–µ–º –ø–æ–∏—Å–∫ —á–µ—Ä–µ–∑ Tavily")
            
            try:
                # –í—ã–ø–æ–ª–Ω—è–µ–º –ø–æ–∏—Å–∫ —á–µ—Ä–µ–∑ Tavily
                tavily_result = search_with_tavily(user_query)
                
                if tavily_result["success"]:
                    logger.info("Tavily –ø–æ–∏—Å–∫ –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ")
                    # –ü–æ–≤—Ç–æ—Ä–Ω–æ –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã —Å —É—á–µ—Ç–æ–º –¥–∞–Ω–Ω—ã—Ö Tavily
                    combined_analysis = analyze_combined_results(user_query, result_2124, result_1620, tavily_result)
                else:
                    logger.warning(f"Tavily –ø–æ–∏—Å–∫ –Ω–µ —É–¥–∞–ª—Å—è: {tavily_result['errors']}")
                    
            except Exception as tavily_error:
                logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–∏—Å–∫–µ —á–µ—Ä–µ–∑ Tavily: {str(tavily_error)}")
        
        # –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        formatted_result = format_combined_analysis_output(combined_analysis)
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ —Å Neo4j —É–∑–ª–∞–º–∏
        dashboard_link = None
        if neo4j_matcher:
            try:
                logger.info("–ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ —Å Neo4j —É–∑–ª–∞–º–∏")
                matching_node_id = neo4j_matcher.find_matching_schetnoe_node(user_query)
                
                if matching_node_id:
                    # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –Ω–∞–π–¥–µ–Ω–Ω–æ–º —É–∑–ª–µ
                    node_info = neo4j_matcher.get_node_info_by_id(matching_node_id)
                    if node_info:
                        node_name = node_info.get('node_name', '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —É–∑–µ–ª')
                        dashboard_url = f"{DASHBOARD_SERVER_URL}/dashboard/{matching_node_id}"
                        
                        dashboard_link = f"\n\nüéØ **–ù–∞–π–¥–µ–Ω —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π –ø–æ–∫–∞–∑–∞—Ç–µ–ª—å –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö:**\n"
                        dashboard_link += f"üìä {node_name}\n"
                        dashboard_link += f"üîó [–ò–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω—ã–π –¥–∞—à–±–æ—Ä–¥]({dashboard_url})\n"
                        dashboard_link += f"üìà –ü—Ä–æ—Å–º–æ—Ç—Ä —Ä–µ–≥–∏–æ–Ω–∞–ª—å–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –∏ –¥–∏–Ω–∞–º–∏–∫–∏"
                        
                        logger.info(f"–ù–∞–π–¥–µ–Ω–æ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ —Å —É–∑–ª–æ–º: {node_name} (ID: {matching_node_id})")
                    
            except Exception as neo4j_error:
                logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ Neo4j —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–π: {str(neo4j_error)}")
        
        # –î–æ–±–∞–≤–ª—è–µ–º —Å—Å—ã–ª–∫—É –Ω–∞ –¥–∞—à–±–æ—Ä–¥ –∫ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—É, –µ—Å–ª–∏ –Ω–∞–π–¥–µ–Ω–∞
        if dashboard_link:
            formatted_result += dashboard_link
        
        logger.info("–ó–∞–ø—Ä–æ—Å —É—Å–ø–µ—à–Ω–æ –æ–±—Ä–∞–±–æ—Ç–∞–Ω")
        return formatted_result
        
    except Exception as e:
        error_msg = f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∑–∞–ø—Ä–æ—Å–∞: {str(e)}"
        logger.error(error_msg, exc_info=True)
        return MESSAGES['error'].format(error=str(e))

